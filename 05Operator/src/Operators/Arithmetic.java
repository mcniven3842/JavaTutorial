package Operators;

public class Arithmetic {

	public static void main(String[] args) {
		int x = 5;
	    int y = 3;
	    System.out.println(x + y);
	    System.out.println(x - y);
	    System.out.println(x * y);
	    System.out.println(x / y);
	    System.out.println(x % y);
	    
	    //increment
	    int a = 5;
	    ++a;
	    System.out.println(a);
	    
	    //decrement
	    int b = 5;
	    --b;
	    System.out.println(b);
	    
	}

}
