package Operators;

public class Assignment {

	public static void main(String[] args) {
	    double x; 
	    x= 5;
	    System.out.println(x);
	    x += 3;
	    System.out.println(x);
	    x -= 3;
	    System.out.println(x);
	    x *= 3;
	    System.out.println(x);
	    x /= 3;
	    System.out.println(x);
	    x %= 3;
	    System.out.println(x);
	    int y;
	    y= 5;
	    y &= 3;
	    System.out.println(y);
	    y |= 3;
	    System.out.println(y);
	    y ^= 3;
	    System.out.println(y);
	    y >>= 3;
	    System.out.println(y);
	    y <<= 3;
	    System.out.println(y);
	}

}
