package Attributes;

public class MultipleAtributes {
	String fname = "John";
	String lname = "Doe";
	int age = 24;
	public static void main(String[] args) {
		MultipleAtributes myObj = new MultipleAtributes();
	    System.out.println("Name: " + myObj.fname + " " + myObj.lname);
	    System.out.println("Age: " + myObj.age);

	}

}
